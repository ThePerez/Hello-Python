### Conditionals ###

my_condition = False

if my_condition:   #   Es lo mismo que if my_condition == True :
    print("Se ejecuta el if")
    
my_condition = 5 * 2

if my_condition == 10:
    print("Se ejecuta el segundo if")
    
if my_condition >= 10:
    print("Es mayor o igual a 10")
else:
    print("Es menor a 10")    

    
print("La ejecución continúa")